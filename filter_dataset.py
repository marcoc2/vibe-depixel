"""
Create a filtered copy of the dataset, removing the top-N% highest color drift pairs.

Reads report.csv (generated by analyze_dataset.py) and copies the clean pairs
to a new directory, leaving the original dataset untouched.

Usage:
  python filter_dataset.py                          # remove top 20% drift
  python filter_dataset.py --top-pct 10             # remove top 10% drift
  python filter_dataset.py --report my_report.csv   # use a different report
  python filter_dataset.py --dry-run                # show what would be copied
"""

import argparse
import csv
import shutil
from pathlib import Path

LR_SRC = "dataset/train/lr"
HR_SRC = "dataset/train/hr"
LR_DST = "dataset/train_cc_clean_50/lr"
HR_DST = "dataset/train_cc_clean_50/hr"
REPORT = "report.csv"


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--report", default=REPORT)
    parser.add_argument("--lr-src", default=LR_SRC)
    parser.add_argument("--hr-src", default=HR_SRC)
    parser.add_argument("--lr-dst", default=LR_DST)
    parser.add_argument("--hr-dst", default=HR_DST)
    parser.add_argument(
        "--top-pct",
        type=float,
        default=20.0,
        help="Percentage of highest-drift pairs to exclude (default: 20)",
    )
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    # Load report
    if not Path(args.report).exists():
        print(f"[error] Report not found: {args.report}")
        print("Run analyze_dataset.py first to generate it.")
        return

    with open(args.report, newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))

    total = len(rows)
    rows.sort(key=lambda r: float(r["color_drift"]))

    n_exclude = round(total * args.top_pct / 100)
    n_keep = total - n_exclude
    keep = rows[:n_keep]
    excluded = rows[n_keep:]

    print(f"Total pairs:  {total}")
    print(
        f"Excluding:    {n_exclude} ({args.top_pct:.0f}% highest drift)  "
        f"drift >= {float(excluded[0]['color_drift']):.2f} LAB"
    )
    print(f"Keeping:      {n_keep}")
    print(f"Destination:  {args.lr_dst} / {args.hr_dst}")

    if args.dry_run:
        print("\n[dry-run] Top 5 excluded:")
        for r in sorted(excluded, key=lambda r: float(r["color_drift"]), reverse=True)[
            :5
        ]:
            print(f"  {r['filename']}  drift={float(r['color_drift']):.2f}")
        return

    # Create destination dirs
    Path(args.lr_dst).mkdir(parents=True, exist_ok=True)
    Path(args.hr_dst).mkdir(parents=True, exist_ok=True)

    # Copy clean pairs
    missing = 0
    for i, row in enumerate(keep, 1):
        name = row["filename"]
        lr_src = Path(args.lr_src) / name
        hr_src = Path(args.hr_src) / name
        if not lr_src.exists() or not hr_src.exists():
            missing += 1
            continue
        shutil.copy2(lr_src, Path(args.lr_dst) / name)
        shutil.copy2(hr_src, Path(args.hr_dst) / name)
        if i % 100 == 0 or i == n_keep:
            print(f"  {i}/{n_keep}", end="\r", flush=True)

    print()
    if missing:
        print(f"[warn] {missing} files not found in source (skipped)")
    print(f"Done. {n_keep - missing} pairs copied to {args.lr_dst} / {args.hr_dst}")
    print(f"\nTo train on the clean dataset:")
    print(
        f"  python main.py --train --lr-dir {args.lr_dst} --hr-dir {args.hr_dst} --preset default"
    )


if __name__ == "__main__":
    main()
