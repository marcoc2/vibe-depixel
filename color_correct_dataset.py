"""
Color correction preprocessing for LR/HR training pairs.

Corrects color drift in HR images (generated by Stable Diffusion) to match
the color distribution of their corresponding LR (pixel art) images.

Methods:
  reinhard   — global LAB mean/std transfer (Reinhard et al. 2001).
               Gentle: preserves SD-generated detail/gradients, only shifts
               the color distribution. Best when drift is moderate.

  histogram  — per-channel histogram specification (skimage).
               Strong: forces HR histogram to match LR. May cause banding
               when LR has very few colors (typical pixel art palette).

  combined   — Reinhard first, then histogram matching blended at 50%.
               Best of both: corrects large shifts without palette snapping.
               Recommended default for pixel art → SD datasets.

  adaptive   — like combined, but blend strength scales with drift severity.
               Pairs with low drift are barely touched; high drift pairs get
               strong correction. Most conservative option.

Usage:
  python color_correct_dataset.py                          # combined, all pairs
  python color_correct_dataset.py --method reinhard
  python color_correct_dataset.py --method adaptive --strength 0.9
  python color_correct_dataset.py --report report.csv      # use drift scores
  python color_correct_dataset.py --dry-run                # stats only, no files
"""

import argparse
import csv
import os
import shutil
from concurrent.futures import ProcessPoolExecutor, as_completed
from pathlib import Path

import numpy as np
from PIL import Image

LR_SRC = "dataset/train/lr"
HR_SRC = "dataset/train/hr"
LR_DST = "dataset/train_cc/lr"
HR_DST = "dataset/train_cc/hr"
EXTENSIONS = {".png", ".jpg", ".jpeg", ".bmp", ".webp"}
SCALE = 4


# ── Color transfer methods ────────────────────────────────────────────────────

def _to_lab(rgb_f32: np.ndarray) -> np.ndarray:
    from skimage.color import rgb2lab
    return rgb2lab(rgb_f32)


def _from_lab(lab: np.ndarray) -> np.ndarray:
    from skimage.color import lab2rgb
    return np.clip(lab2rgb(lab).astype(np.float32), 0.0, 1.0)


def reinhard(lr_ref: np.ndarray, hr: np.ndarray) -> np.ndarray:
    """Transfer color distribution from lr_ref to hr in LAB space.
    lr_ref and hr are float32 RGB [0, 1], same spatial size."""
    src_lab = _to_lab(lr_ref)
    tgt_lab = _to_lab(hr)
    result = tgt_lab.copy()
    for c in range(3):
        src_mean = src_lab[..., c].mean()
        src_std  = src_lab[..., c].std()
        tgt_mean = tgt_lab[..., c].mean()
        tgt_std  = tgt_lab[..., c].std()
        if tgt_std < 1e-6:
            continue
        result[..., c] = (tgt_lab[..., c] - tgt_mean) * (src_std / tgt_std) + src_mean
    return _from_lab(result)


def histogram_match(lr_ref: np.ndarray, hr: np.ndarray) -> np.ndarray:
    """Per-channel histogram specification: force hr to match lr_ref's distribution."""
    from skimage.exposure import match_histograms
    matched = match_histograms(hr, lr_ref, channel_axis=2)
    return np.clip(matched.astype(np.float32), 0.0, 1.0)


def combined(lr_ref: np.ndarray, hr: np.ndarray, hist_blend: float = 0.5) -> np.ndarray:
    """Reinhard then partial histogram match. hist_blend=0 → pure Reinhard,
    hist_blend=1 → pure histogram after Reinhard."""
    r = reinhard(lr_ref, hr)
    h = histogram_match(lr_ref, r)
    return np.clip((1.0 - hist_blend) * r + hist_blend * h, 0.0, 1.0)


def adaptive(lr_ref: np.ndarray, hr: np.ndarray,
             drift: float, max_strength: float = 0.9) -> np.ndarray:
    """Scale correction strength with drift severity.
    Low drift → barely touched. High drift → strong combined correction."""
    # Normalise drift to [0, 1] based on observed range (0–16 LAB units)
    alpha = min(drift / 16.0, 1.0) * max_strength
    corrected = combined(lr_ref, hr, hist_blend=0.5)
    return np.clip((1.0 - alpha) * hr + alpha * corrected, 0.0, 1.0)


def _color_drift_lab(a: np.ndarray, b: np.ndarray) -> float:
    """Distributional color distance: mean absolute difference of per-channel
    LAB means and stds. More meaningful than pixel-level MAE for pixel art → SD
    because spatial structure differs by design (blocks vs gradients)."""
    a_lab = _to_lab(a)
    b_lab = _to_lab(b)
    diff = 0.0
    for c in range(3):
        diff += abs(a_lab[..., c].mean() - b_lab[..., c].mean())
        diff += abs(a_lab[..., c].std()  - b_lab[..., c].std())
    return diff / 6.0  # average across 3 channels × 2 stats


# ── Worker (top-level for ProcessPoolExecutor pickling) ───────────────────────

def _process_pair(args: tuple) -> dict:
    lr_src, hr_src, lr_dst, hr_dst, method, strength, drift = args
    try:
        lr_arr = np.array(Image.open(lr_src).convert("RGB")).astype(np.float32) / 255.0
        hr_arr = np.array(Image.open(hr_src).convert("RGB")).astype(np.float32) / 255.0

        hh, hw = hr_arr.shape[:2]
        lr_ref = np.array(
            Image.fromarray((lr_arr * 255).astype(np.uint8)).resize((hw, hh), Image.NEAREST)
        ).astype(np.float32) / 255.0

        drift_before = _color_drift_lab(lr_ref, hr_arr)

        if method == "reinhard":
            hr_out = reinhard(lr_ref, hr_arr)
        elif method == "histogram":
            hr_out = histogram_match(lr_ref, hr_arr)
        elif method == "combined":
            hr_out = combined(lr_ref, hr_arr, hist_blend=strength)
        elif method == "adaptive":
            hr_out = adaptive(lr_ref, hr_arr, drift=drift_before, max_strength=strength)
        else:
            raise ValueError(f"Unknown method: {method}")

        drift_after = _color_drift_lab(lr_ref, hr_out)

        # Save
        shutil.copy2(lr_src, lr_dst)
        Image.fromarray((hr_out * 255).astype(np.uint8)).save(hr_dst)

        return {
            "filename": Path(lr_src).name,
            "drift_before": round(drift_before, 4),
            "drift_after": round(drift_after, 4),
            "reduction": round(drift_before - drift_after, 4),
            "ok": True,
        }
    except Exception as e:
        return {"filename": Path(lr_src).name, "drift_before": 0,
                "drift_after": 0, "reduction": 0, "ok": False, "error": str(e)}


# ── File listing ──────────────────────────────────────────────────────────────

def _list_pairs(lr_dir: str, hr_dir: str) -> list[tuple[Path, Path]]:
    lr_names = sorted(p.name for p in Path(lr_dir).iterdir()
                      if p.suffix.lower() in EXTENSIONS)
    hr_names = set(p.name for p in Path(hr_dir).iterdir()
                   if p.suffix.lower() in EXTENSIONS)
    matched = [n for n in lr_names if n in hr_names]
    return [(Path(lr_dir) / n, Path(hr_dir) / n) for n in matched]


def _load_drift_map(report_path: str) -> dict[str, float]:
    if not Path(report_path).exists():
        return {}
    with open(report_path, newline="", encoding="utf-8") as f:
        return {row["filename"]: float(row["color_drift"]) for row in csv.DictReader(f)}


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--lr-src", default=LR_SRC)
    parser.add_argument("--hr-src", default=HR_SRC)
    parser.add_argument("--lr-dst", default=LR_DST)
    parser.add_argument("--hr-dst", default=HR_DST)
    parser.add_argument("--method", default="combined",
                        choices=["reinhard", "histogram", "combined", "adaptive"])
    parser.add_argument("--strength", type=float, default=0.5,
                        help="Histogram blend (combined) or max correction (adaptive). "
                             "Range 0-1, default 0.5")
    parser.add_argument("--report", default="report.csv",
                        help="report.csv from analyze_dataset.py (used by adaptive method)")
    parser.add_argument("--workers", type=int, default=min(8, os.cpu_count() or 4))
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    pairs = _list_pairs(args.lr_src, args.hr_src)
    drift_map = _load_drift_map(args.report)

    print(f"Pairs:    {len(pairs)}")
    print(f"Method:   {args.method}  (strength={args.strength})")
    print(f"Output:   {args.lr_dst} / {args.hr_dst}")
    print(f"Workers:  {args.workers}")

    if args.dry_run:
        # Sample 5 pairs and show expected drift reduction
        import random
        samples = random.sample(pairs, min(5, len(pairs)))
        print("\n[dry-run] Sample drift estimates (first 5 random pairs):")
        for lr_p, hr_p in samples:
            lr_arr = np.array(Image.open(lr_p).convert("RGB")).astype(np.float32) / 255.0
            hr_arr = np.array(Image.open(hr_p).convert("RGB")).astype(np.float32) / 255.0
            hh, hw = hr_arr.shape[:2]
            lr_ref = np.array(
                Image.fromarray((lr_arr * 255).astype(np.uint8)).resize((hw, hh), Image.NEAREST)
            ).astype(np.float32) / 255.0
            before = _color_drift_lab(lr_ref, hr_arr)
            print(f"  {lr_p.name:<35} drift={before:.2f} LAB")
        return

    Path(args.lr_dst).mkdir(parents=True, exist_ok=True)
    Path(args.hr_dst).mkdir(parents=True, exist_ok=True)

    try:
        from tqdm import tqdm
        _tqdm = tqdm
    except ImportError:
        _tqdm = None

    tasks = [
        (
            str(lr_p), str(hr_p),
            str(Path(args.lr_dst) / lr_p.name),
            str(Path(args.hr_dst) / hr_p.name),
            args.method, args.strength,
            drift_map.get(lr_p.name, 5.0),
        )
        for lr_p, hr_p in pairs
    ]

    results = []
    with ProcessPoolExecutor(max_workers=args.workers) as pool:
        futures = {pool.submit(_process_pair, t): i for i, t in enumerate(tasks)}
        it = _tqdm(as_completed(futures), total=len(futures), unit="img",
                   dynamic_ncols=True) if _tqdm else as_completed(futures)
        done = 0
        for fut in it:
            r = fut.result()
            results.append(r)
            if not _tqdm:
                done += 1
                if done % 100 == 0 or done == len(tasks):
                    print(f"  {done}/{len(tasks)}", end="\r", flush=True)

    if not _tqdm:
        print()

    ok = [r for r in results if r["ok"]]
    failed = [r for r in results if not r["ok"]]

    before = np.mean([r["drift_before"] for r in ok])
    after  = np.mean([r["drift_after"]  for r in ok])
    reduction = (before - after) / before * 100 if before > 0 else 0

    print(f"\n{'=' * 55}")
    print(f"  Processed: {len(ok)}  |  Failed: {len(failed)}")
    print(f"  Color drift before: {before:.3f} LAB")
    print(f"  Color drift after:  {after:.3f} LAB")
    print(f"  Reduction:          {reduction:.1f}%")
    print(f"{'=' * 55}")

    if failed:
        for r in failed[:5]:
            print(f"  [warn] {r['filename']}: {r.get('error')}")

    print(f"\nTo train on the corrected dataset:")
    print(f"  python experiments.py --lr-dir {args.lr_dst} --hr-dir {args.hr_dst}")


if __name__ == "__main__":
    main()
